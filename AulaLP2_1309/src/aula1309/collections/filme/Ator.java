
package aula1309.collections.filme;


public class Ator {
    String ator;

    public Ator(String ator) {
        this.ator = ator;
    }

    public String getAtor() {
        return ator;
    }

    public void setAtor(String ator) {
        this.ator = ator;
    }

    @Override
    public String toString() {
        return "Ator{" + "ator=" + ator + '}';
    }
    
}
